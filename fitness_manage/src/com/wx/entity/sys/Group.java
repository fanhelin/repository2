package com.wx.entity.sys;

public class Group {
  int id ;
  String group_code;
  String name ;
  
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getGroup_code() {
	return group_code;
}
public void setGroup_code(String group_code) {
	this.group_code = group_code;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
  
  
}
